    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // 1 pegar os dados e colocar na variavel
        String u, s;
        u = txtUsuario.getText();
        s = pswSenha.getText();

        try {
            // 2 conectar com banco de dados
            ResultSet resultado = new AppDao().entrar(u, s);
            //vetifica se tem os dados do usuario
            if (resultado.next()) {
                // abrir o menu e manda o id do usuario
                int id;
                String isVendedor;
                id = resultado.getInt("idUsuario");
                isVendedor = resultado.getString("e_vendedor");
                dispose();
                new Menu(id, isVendedor).setVisible(true);
            } //n√£o encontrou o usuario
            else {
                JOptionPane.showMessageDialog(null, "Usuario e/ou senha invalidos");
            }
        } catch (ClassNotFoundException | SQLException ex) {

            JOptionPane.showMessageDialog(null, "Entre contado com administrador e informe o erro:" + ex.getMessage());

        }
    }//GEN-LAST:event_btnEntrarActionPerformed

