    private void btnReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservaActionPerformed

        try {

            if (Utilitarios.validarDataValidade(jTextDia.getText())) {
                reserva.setData(jTextDia.getText());
                reserva.setHorario(jComboBoxHorario.getSelectedItem().toString());

                new AppDao().salvarReserva(this.reserva);

                JOptionPane.showMessageDialog(null, "Reservado com sucesso: \n" + reserva);
                dispose();
                new Menu(this.reserva.getIdComprador()).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Data Invalida!!");
            }
        } catch (ClassNotFoundException ex) {
            //import javax.swing.JOptionPane;
            JOptionPane.showMessageDialog(null, "Erro ao tentar localizar Driver JDBC");
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o banco de dados:" + ex.getMessage());

        }

    }//GEN-LAST:event_btnReservaActionPerformed

